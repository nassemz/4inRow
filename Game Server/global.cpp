const int StringToInt(string k)
{
	const char* temp=k.c_str();
	for(int indx=0;indx<k.length();indx++)
	{
		if(temp[indx]>'9' || temp[indx]<'0')
			return 0;
	}
	return atoi(k.c_str());
}
